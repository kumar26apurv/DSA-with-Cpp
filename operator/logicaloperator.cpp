// CPP Program to demonstrate the Logical Operators
#include <iostream>
using namespace std;

int main()
{
	int a = 6, b = 4;

	// Logical AND operator
	cout << "a && b is " << (a && b) << endl;

	// Logical OR operator
	cout << "a ! b is " << (a > b) << endl;

	// Logical NOT operator
	cout << "!a is " << (!a) << endl;

	return 0;
}
